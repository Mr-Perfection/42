#include "libft.h"

void	ft_strrev(char *str)
{
	int		i;
	int		j;
	char	tmp;

	if (str)
	{
		i = 0;
		j = ft_strlen(str) - 1;
		while (j > i)
		{
			tmp = str[i];
			str[i++] = str[j];
			str[j--] = tmp;
		}
	}
}

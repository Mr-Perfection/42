#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t max)
{
	char	*str;
	size_t	i;

	str = s1;
	i = 0;
	if (*s1)
		while (*s1)
			++s1;
	while ((max-- && *s2))
		*s1++ = *s2++;
	s1[0] = '\0';
	return (str);
}

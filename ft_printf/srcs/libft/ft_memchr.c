#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	c_char;
	char	*c_src;

	c_char = (char)c;
	c_src = (char*)s;
	while (n--)
		if (*c_src++ == c_char)
			return ((void*)(--c_src));
	return (NULL);
}

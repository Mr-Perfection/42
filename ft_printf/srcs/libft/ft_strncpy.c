#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	char	*ptr;

	ptr = dst;
	while (len && *src)
	{
		*ptr++ = *src++;
		len--;
	}
	while (len--)
		*ptr++ = '\0';
	return (dst);
}

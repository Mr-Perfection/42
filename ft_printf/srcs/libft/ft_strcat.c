#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*s1_ptr;

	s1_ptr = s1 + ft_strlen(s1);
	while (*s2)
		*s1_ptr++ = *s2++;
	*s1_ptr = '\0';
	return (s1);
}

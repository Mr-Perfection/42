#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*ptr;

	ptr = (char*)s + ft_strlen(s);
	while (ptr != s)
		if (*ptr-- == c)
			return (++ptr);
	if (*ptr == c)
		return (ptr);
	return (NULL);
}

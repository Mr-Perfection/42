#include "libft.h"

void	ft_putstrarr(char **arr)
{
	write(1, "[", 1);
	if (*arr)
		ft_putstr(*arr++);
	while (*arr)
	{
		write(1, ", ", 2);
		ft_putstr(*arr++);
	}
	write(1, "]", 1);
}

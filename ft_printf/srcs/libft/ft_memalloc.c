#include "libft.h"

void	*ft_memalloc(size_t size)
{
	char	*ptr;
	void	*result;

	if ((result = malloc(size)) == NULL)
		return (NULL);
	ptr = (char*)result;
	while (size--)
		*ptr++ = 0;
	return (result);
}

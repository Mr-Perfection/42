#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	n;
	char	*ptr;
	char	*result;

	n = ft_strlen(s1);
	if ((result = ft_strnew(n)) == NULL)
		return (NULL);
	ptr = result;
	while (n--)
		*ptr++ = *s1++;
	return (result);
}
